function conceptToHash(label) {
  return `#/concept/${encodeURIComponent(normalizeConcept(label) || "自由")}`;
}

function conceptFromHash() {
  if (typeof window === "undefined" || !window.location) return "";
  const hash = window.location.hash || "";
  const match = hash.match(/^#\/concept\/(.+)$/);
  if (!match) return "";
  try {
    return normalizeConcept(decodeURIComponent(match[1]));
  } catch {
    return "";
  }
}

function syncConceptHash(label, replace = false) {
  if (typeof window === "undefined" || !window.history || !window.location) return;
  const nextHash = conceptToHash(label);
  if (window.location.hash === nextHash) return;
  if (replace) window.history.replaceState(null, "", nextHash);
  else window.history.pushState(null, "", nextHash);
}

function relationId(source, target, relation) {
  return `${source}→${target}::${relation}`;
}

function buildGraph(rootLabel) {
  const root = normalizeConcept(rootLabel) || "自由";
  const system = systemProfileFor(root);
  const maxNodes = Number(state.scope);
  const nodes = [];
  const links = [];
  const nodeMap = new Map();
  const linkSet = new Set();
  const rand = randomFrom(hashString(`${root}:${state.generation}:fresh-system`));

  function addNode(label, depth, viaType, weight = 1) {
    const clean = normalizeConcept(label);
    if (!clean) return null;
    if (nodeMap.has(clean)) {
      const existing = nodeMap.get(clean);
      existing.weight += weight * 0.15;
      existing.depth = Math.min(existing.depth, depth);
      return existing;
    }

    const category = depth === 0 ? "中心" : inferCategory(clean);
    const meta = CATEGORY_META[category] || CATEGORY_META.问题域;
    const angle = meta.angle + (rand() - 0.5) * 0.85;
    const radius = depth === 0 ? 0 : 120 + depth * 118 + rand() * 80;
    const node = {
      id: clean,
      label: clean,
      category,
      type: viaType || category,
      summary: summaryFor(clean),
      depth,
      weight,
      x: Math.cos(angle) * radius + (rand() - 0.5) * 80,
      y: Math.sin(angle) * radius + (rand() - 0.5) * 80,
      vx: 0,
      vy: 0,
      r: depth === 0 ? 28 : Math.max(5, 17 - depth * 3 + weight * 2.5),
      fixed: false
    };
    nodeMap.set(clean, node);
    nodes.push(node);
    return node;
  }

  function addLink(sourceLabel, targetLabel, relation, strength = 0.58, depth = 1) {
    const source = addNode(sourceLabel, sourceLabel === root ? 0 : depth, inferCategory(sourceLabel), 0.2);
    const target = addNode(targetLabel, depth, relation.type, relation.strength || 0.5);
    if (!source || !target || source.id === target.id) return;
    const id = relationId(source.id, target.id, relation.relation || "相关");
    if (linkSet.has(id)) return;
    linkSet.add(id);
    links.push({
      id,
      source,
      target,
      label: relation.relation || "相关",
      category: relation.type || target.category,
      strength,
      depth
    });
  }

  addNode(root, 0, "中心概念", 5);
  const direct = getRelations(root, 0).slice(0, Math.min(54, Math.floor(maxNodes * 0.42)));
  direct.forEach((relation) => addLink(root, relation.to, relation, relation.strength || 0.78, 1));

  const secondBudget = Math.max(2, Math.floor((maxNodes - nodes.length) / Math.max(1, direct.length)));
  direct.forEach((relation, index) => {
    if (nodes.length >= maxNodes) return;
    const relationList = getRelations(relation.to, 1);
    const count = Math.min(index < 8 ? secondBudget + 3 : secondBudget, relationList.length);
    relationList.slice(0, count).forEach((child) => {
      if (nodes.length < maxNodes) addLink(relation.to, child.to, child, (child.strength || 0.54) * 0.78, 2);
    });
  });

  if (maxNodes > 160) {
    const layerTwo = nodes.filter((node) => node.depth === 2).slice(0, Math.floor((maxNodes - 150) / 2) + 12);
    layerTwo.forEach((node) => {
      if (nodes.length >= maxNodes) return;
      getRelations(node.label, 2)
        .slice(0, 2)
        .forEach((child) => {
          if (nodes.length < maxNodes) addLink(node.label, child.to, child, (child.strength || 0.48) * 0.58, 3);
        });
    });
  }

  fillRemainingNodes(nodes, addLink, root, maxNodes);
  addFreshSystemLinks(nodes, addLink, root, system);
  addCrossLinks(nodes, links, linkSet, root);
  return { nodes, links };
}


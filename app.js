function getRelations(label, depth = 0) {
  const explicit = CORE_GRAPH[label]?.relations || [];
  const incoming = [];
  Object.entries(CORE_GRAPH).forEach(([source, entry]) => {
    entry.relations.forEach((relation) => {
      if (relation.to === label && source !== label) {
        incoming.push(R(source, `${source}指向${label}`, entry.type, Math.max(0.52, relation.strength - 0.08)));
      }
    });
  });

  const generated = generatedRelationsFor(label, depth);
  const merged = [...explicit, ...incoming, ...generated];
  const seen = new Set();
  return merged.filter((relation) => {
    if (seen.has(relation.to) || relation.to === label) return false;
    seen.add(relation.to);
    return true;
  });
}


function conceptDefinitionFor(label, category) {
  const concept = normalizeConcept(label);
  const axisDefinition = axisDefinitionFor(concept);
  if (axisDefinition) return axisDefinition;
  const scholar = scholarRecordFor(concept);
  if (scholar) return scholarDefinitionFor(concept, scholar);
  const resolvedCategory = category || inferCategory(concept);
  const system = systemProfileFor(concept);
  const relations = rankedDefinitionRelations(concept);
  const lead = CORE_GRAPH[concept]?.summary || definitionLeadFor(concept, resolvedCategory, system, relations);
  const detail = definitionDetailFor(concept, resolvedCategory, relations, system);
  return `${concept}是什么：${lead} ${detail}`;
}


function uniqueList(items, limit = 6) {
  return [...new Set(items.filter(Boolean))].slice(0, limit);
}

function connectedConceptsFor(label, limit = 8) {
  return uniqueList(
    state.graph.links
      .filter((link) => link.source.label === label || link.target.label === label)
      .sort((a, b) => b.strength - a.strength)
      .map((link) => (link.source.label === label ? link.target.label : link.source.label)),
    limit
  );
}

function fallbackText(items, fallback) {
  return items.length ? items.join("、") : fallback;
}

function relationLabels(relations, types, limit = 5) {
  return uniqueList(
    relations
      .filter((relation) => types.includes(relation.type))
      .map((relation) => relation.to),
    limit
  );
}

function relationClaims(relations, limit = 5) {
  const seen = new Set();
  const claims = [];
  relations.forEach((relation) => {
    if (seen.has(relation.to)) return;
    seen.add(relation.to);
    const action = relation.relation || "相关";
    claims.push(
      /^(体系概念|代表思想家|研究方法|核心争论)\s*\d+$/.test(action)
        ? relation.to
        : `${relation.to}（${action}）`
    );
  });
  return claims.slice(0, limit);
}

function isGeneratedFrameLabel(concept, label) {
  return (
    label.startsWith(`${concept}的`) ||
    label.startsWith(`${concept}中的`) ||
    label.startsWith(`${concept}：`) ||
    /^(问题核心|概念边界|经典争论|实践场景|当代变体)$/.test(label)
  );
}

function rankedDefinitionRelations(concept) {
  if (CORE_GRAPH[concept]) {
    const explicit = CORE_GRAPH[concept].relations || [];
    const incoming = [];
    Object.entries(CORE_GRAPH).forEach(([source, entry]) => {
      entry.relations.forEach((relation) => {
        if (relation.to === concept && source !== concept) {
          incoming.push(R(source, `${source}指向${concept}`, entry.type, Math.max(0.52, relation.strength - 0.08)));
        }
      });
    });
    return [...explicit, ...incoming].sort((a, b) => (b.strength || 0.5) - (a.strength || 0.5));
  }

  const relations = getRelations(concept, 0).sort((a, b) => (b.strength || 0.5) - (a.strength || 0.5));
  const semanticRelations = relations.filter((relation) => !isGeneratedFrameLabel(concept, relation.to));
  return semanticRelations.length ? semanticRelations : relations;
}

function listOr(items, fallback) {
  return items.length ? items.join("、") : fallback;
}


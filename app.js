function scholarPrimaryCategoryFor(scholar) {
  return scholar?.categories?.find((category) => !["人物", "问题域", "流派"].includes(category))
    || scholar?.categories?.[0]
    || "问题域";
}

function dominantScholarCategoryFor(concept, category) {
  if (category !== "人物") return category;
  const scholar = scholarRecordFor(concept);
  if (scholar) return scholarPrimaryCategoryFor(scholar);
  const counts = {};
  rankedDefinitionRelations(concept).forEach((relation) => {
    if (!CONTEMPORARY_SCHOLARS[relation.type]) return;
    counts[relation.type] = (counts[relation.type] || 0) + (relation.strength || 0.5);
  });
  const best = Object.entries(counts).sort((a, b) => b[1] - a[1])[0];
  return best?.[0] || "人物";
}

function contemporaryScholarsFor(concept, category, limit = 7) {
  const clean = normalizeConcept(concept);
  const primaryCategory = dominantScholarCategoryFor(clean, category || inferCategory(clean));
  const candidates = [];

  CONTEMPORARY_KEYWORD_SCHOLARS.forEach((rule) => {
    if (rule.words.some((word) => clean.includes(word))) {
      rule.names.forEach((name) => {
        const scholar = scholarByName(name);
        if (scholar) candidates.push(scholar);
      });
    }
  });

  (CONTEMPORARY_SCHOLARS[primaryCategory] || []).forEach((scholar) => candidates.push(scholar));
  if (primaryCategory !== "问题域") {
    CONTEMPORARY_SCHOLARS.问题域.forEach((scholar) => candidates.push(scholar));
  }

  const ranked = candidates
    .map((scholar, index) => {
      const tagScore = scholar.tags.filter((tag) => clean.includes(tag) || tag.includes(clean)).length * 4;
      const categoryScore = scholar.categories?.includes(primaryCategory) ? 3 : 0;
      return { scholar, score: tagScore + categoryScore + Math.max(0, 2 - index * 0.01) };
    })
    .sort((a, b) => b.score - a.score)
    .map((entry) => entry.scholar);

  const seen = new Set();
  return ranked.filter((scholar) => {
    if (!scholar || seen.has(scholar.name)) return false;
    seen.add(scholar.name);
    return true;
  }).slice(0, limit);
}


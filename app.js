function fillRemainingNodes(nodes, addLink, root, maxNodes) {
  const anchors = nodes
    .filter((node) => node.depth > 0)
    .sort((a, b) => a.depth - b.depth || b.weight - a.weight)
    .slice(0, 70);
  if (!anchors.length) return;

  let cursor = 0;
  let attempts = 0;
  while (nodes.length < maxNodes && attempts < maxNodes * 8) {
    const anchor = anchors[cursor % anchors.length];
    const seed = hashString(`${root}:${anchor.label}:${attempts}`);
    const poolItem = PHILOSOPHY_POOL[seed % PHILOSOPHY_POOL.length];
    const base = compactBase(anchor.label).slice(0, 9);
    const target = `${base}与${poolItem}`;
    if (target !== anchor.label) {
      addLink(
        anchor.label,
        target,
        R(target, `比较${poolItem}`, inferCategory(poolItem), 0.34),
        0.22,
        Math.min(3, anchor.depth + 1)
      );
    }
    cursor += 1;
    attempts += 1;
  }
}

function addCrossLinks(nodes, links, linkSet, root) {
  const byLabel = new Map(nodes.map((node) => [node.label, node]));
  const addGraphLink = (source, target, label, category, strength = 0.2, depth = 2) => {
    if (!source || !target || source === target) return;
    const id = relationId(source.label, target.label, label);
    if (linkSet.has(id)) return;
    linkSet.add(id);
    links.push({
      id,
      source,
      target,
      label,
      category: category || target.category,
      strength,
      depth: Math.max(depth, source.depth, target.depth)
    });
  };

  nodes.forEach((node) => {
    const entry = CORE_GRAPH[node.label];
    if (!entry) return;
    entry.relations.forEach((relation) => {
      if (!byLabel.has(relation.to)) return;
      const id = relationId(node.label, relation.to, relation.relation);
      if (linkSet.has(id)) return;
      linkSet.add(id);
      links.push({
        id,
        source: node,
        target: byLabel.get(relation.to),
        label: relation.relation,
        category: relation.type,
        strength: Math.max(0.22, (relation.strength || 0.5) * 0.46),
        depth: Math.max(node.depth, byLabel.get(relation.to).depth)
      });
    });
  });

  const grouped = new Map();
  nodes.forEach((node) => {
    if (node.label === root) return;
    const list = grouped.get(node.category) || [];
    list.push(node);
    grouped.set(node.category, list);
  });

  grouped.forEach((list) => {
    const local = list.slice(0, 28);
    local.forEach((source, index) => {
      const target = list[index + 1];
      if (target) addGraphLink(source, target, "同域邻近", source.category, 0.18, 2);
      const skipTarget = list[index + 3];
      if (skipTarget) addGraphLink(source, skipTarget, "同域交叉", source.category, 0.14, 3);
    });
  });

  const rootNode = byLabel.get(root);
  const mainAxis = [
    `${root}的问题核心`,
    `${root}的概念边界`,
    `${root}的经典争论`,
    `${root}的实践场景`,
    `${root}的当代变体`
  ].map((label) => byLabel.get(label)).filter(Boolean);
  const categoryRepresentatives = [...grouped.values()]
    .map((list) => list.slice().sort((a, b) => a.depth - b.depth || b.weight - a.weight)[0])
    .filter(Boolean);

  categoryRepresentatives.forEach((node, index) => {
    const next = categoryRepresentatives[(index + 1) % categoryRepresentatives.length];
    if (next && next !== node) addGraphLink(node, next, "跨领域桥接", next.category, 0.2, 2);

    const axis = mainAxis[index % Math.max(1, mainAxis.length)] || rootNode;
    if (axis) addGraphLink(axis, node, "领域入口", node.category, 0.26, 2);
  });

  const degree = new Map(nodes.map((node) => [node.label, 0]));
  links.forEach((link) => {
    degree.set(link.source.label, (degree.get(link.source.label) || 0) + 1);
    degree.set(link.target.label, (degree.get(link.target.label) || 0) + 1);
  });
  nodes
    .filter((node) => node.label !== root && (degree.get(node.label) || 0) < 2)
    .slice(0, 80)
    .forEach((node, index) => {
      const axis = mainAxis[index % Math.max(1, mainAxis.length)] || categoryRepresentatives.find((item) => item.category === node.category) || rootNode;
      if (axis) addGraphLink(axis, node, "连通补强", node.category, 0.18, 3);
    });

  nodes.slice(0, 120).forEach((node, index) => {
    if (node.label === root) return;
    const offset = (hashString(`${root}:${node.label}:bridge`) % 17) + 5;
    const target = nodes[(index + offset) % nodes.length];
    if (target && target.label !== root && target.category !== node.category) {
      addGraphLink(node, target, "跨域联想", target.category, 0.11, 3);
    }
  });
}

function setRoot(label, pushHistory = true, options = {}) {
  const next = normalizeConcept(label) || "自由";
  const isFreshSystem = options.forceFresh || next !== state.root || !state.graph.nodes.length;
  if (isFreshSystem) state.generation += 1;
  state.root = next;
  state.selected = next;
  state.system = systemProfileFor(next);
  conceptInput.value = next;
  state.hover = null;
  state.drag = null;
  state.pan = null;
  tooltip.hidden = true;
  state.graph = { nodes: [], links: [] };
  state.graph = buildGraph(next);
  state.alpha = 1;
  resetView();

  if (pushHistory) {
    state.history = [next, ...state.history.filter((item) => item !== next)].slice(0, 18);
    saveHistory();
  }
  if (!options.fromHash && options.updateHash !== false) {
    syncConceptHash(next, Boolean(options.replaceHash));
  }
  updateInspector();
}

function resetView() {
  const rect = canvas.getBoundingClientRect();
  const width = state.size.width || rect.width || 1;
  const height = state.size.height || rect.height || 1;
  const nodes = state.graph.nodes || [];

  if (!nodes.length) {
    state.view.scale = 1;
    state.view.x = width / 2;
    state.view.y = height / 2;
    return;
  }

  const bounds = nodes.reduce(
    (box, node) => ({
      minX: Math.min(box.minX, node.x - node.r),
      maxX: Math.max(box.maxX, node.x + node.r),
      minY: Math.min(box.minY, node.y - node.r),
      maxY: Math.max(box.maxY, node.y + node.r)
    }),
    { minX: Infinity, maxX: -Infinity, minY: Infinity, maxY: -Infinity }
  );
  const graphWidth = Math.max(1, bounds.maxX - bounds.minX);
  const graphHeight = Math.max(1, bounds.maxY - bounds.minY);
  const padding = width < 720 ? 34 : 72;
  const scale = clamp(
    Math.min((width - padding * 2) / graphWidth, (height - padding * 2) / graphHeight),
    0.32,
    1.35
  );
  const centerX = (bounds.minX + bounds.maxX) / 2;
  const centerY = (bounds.minY + bounds.maxY) / 2;

  state.view.scale = Number.isFinite(scale) ? scale : 1;
  state.view.x = width / 2 - centerX * state.view.scale;
  state.view.y = height / 2 - centerY * state.view.scale;
  state.hover = null;
  tooltip.hidden = true;
}

function resizeCanvas() {
  const rect = canvas.getBoundingClientRect();
  const dpr = Math.max(1, Math.min(2, window.devicePixelRatio || 1));
  state.size = { width: rect.width, height: rect.height, dpr };
  canvas.width = Math.max(1, Math.floor(rect.width * dpr));
  canvas.height = Math.max(1, Math.floor(rect.height * dpr));
  ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
  if (!state.view.x || !state.view.y) resetView();
}

function clusterTarget(node) {
  if (node.depth === 0) return { x: 0, y: 0 };
  const meta = CATEGORY_META[node.category] || CATEGORY_META.问题域;
  const radius = 150 + node.depth * 135;
  return {
    x: Math.cos(meta.angle) * radius,
    y: Math.sin(meta.angle) * radius
  };
}

function stepSimulation() {
  const { nodes, links } = state.graph;
  const alpha = state.alpha;
  if (alpha < 0.01) return;

  links.forEach((link) => {
    const dx = link.target.x - link.source.x;
    const dy = link.target.y - link.source.y;
    const dist = Math.sqrt(dx * dx + dy * dy) || 1;
    const desired = link.depth === 1 ? 116 : link.depth === 2 ? 96 : 82;
    const force = (dist - desired) * 0.0045 * link.strength * alpha;
    const fx = (dx / dist) * force;
    const fy = (dy / dist) * force;
    if (!link.source.fixed) {
      link.source.vx += fx;
      link.source.vy += fy;
    }
    if (!link.target.fixed) {
      link.target.vx -= fx;
      link.target.vy -= fy;
    }
  });

  for (let i = 0; i < nodes.length; i += 1) {
    const a = nodes[i];
    for (let j = i + 1; j < nodes.length; j += 1) {
      const b = nodes[j];
      const dx = b.x - a.x;
      const dy = b.y - a.y;
      let distSq = dx * dx + dy * dy;
      if (distSq < 0.01) distSq = 0.01;
      const dist = Math.sqrt(distSq);
      const minDist = a.r + b.r + 10;
      const charge = ((a.depth === 0 || b.depth === 0) ? 980 : 420) / distSq;
      const push = Math.min(1.2, charge) * alpha;
      const px = (dx / dist) * push;
      const py = (dy / dist) * push;
      if (!a.fixed) {
        a.vx -= px;
        a.vy -= py;
      }
      if (!b.fixed) {
        b.vx += px;
        b.vy += py;
      }
      if (dist < minDist) {
        const overlap = (minDist - dist) * 0.012 * alpha;
        const ox = (dx / dist) * overlap;
        const oy = (dy / dist) * overlap;
        if (!a.fixed) {
          a.vx -= ox;
          a.vy -= oy;
        }
        if (!b.fixed) {
          b.vx += ox;
          b.vy += oy;
        }
      }
    }
  }

  nodes.forEach((node) => {
    const target = clusterTarget(node);
    const centerForce = node.depth === 0 ? 0.045 : 0.0018 + node.depth * 0.0006;
    if (!node.fixed) {
      node.vx += (target.x - node.x) * centerForce * alpha;
      node.vy += (target.y - node.y) * centerForce * alpha;
      node.vx *= 0.88;
      node.vy *= 0.88;
      node.x += node.vx;
      node.y += node.vy;
    }
  });

  state.alpha *= 0.986;
}

function worldToScreen(point) {
  return {
    x: state.view.x + point.x * state.view.scale,
    y: state.view.y + point.y * state.view.scale
  };
}

function screenToWorld(point) {
  return {
    x: (point.x - state.view.x) / state.view.scale,
    y: (point.y - state.view.y) / state.view.scale
  };
}

function colorFor(category) {
  return (CATEGORY_META[category] || CATEGORY_META.问题域).color;
}

function rgba(hex, alpha) {
  const value = hex.replace("#", "");
  const r = parseInt(value.slice(0, 2), 16);
  const g = parseInt(value.slice(2, 4), 16);
  const b = parseInt(value.slice(4, 6), 16);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function truncateText(text, maxWidth, font) {
  ctx.font = font;
  if (ctx.measureText(text).width <= maxWidth) return text;
  let output = text;
  while (output.length > 2 && ctx.measureText(`${output}…`).width > maxWidth) {
    output = output.slice(0, -1);
  }
  return `${output}…`;
}

function draw() {
  const { width, height, dpr } = state.size;
  ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
  ctx.clearRect(0, 0, width, height);

  drawLinks();
  drawNodes();
  drawLabels();
}

function drawLinks() {
  const hovered = state.hover?.label;
  state.graph.links.forEach((link) => {
    const a = worldToScreen(link.source);
    const b = worldToScreen(link.target);
    const dx = b.x - a.x;
    const dy = b.y - a.y;
    const dist = Math.sqrt(dx * dx + dy * dy) || 1;
    const normalX = -dy / dist;
    const normalY = dx / dist;
    const bend = (hashString(link.id) % 21) - 10;
    const cx = (a.x + b.x) / 2 + normalX * bend;
    const cy = (a.y + b.y) / 2 + normalY * bend;
    const active = hovered && (link.source.label === hovered || link.target.label === hovered);
    const color = colorFor(link.category);

    ctx.beginPath();
    ctx.moveTo(a.x, a.y);
    ctx.quadraticCurveTo(cx, cy, b.x, b.y);
    ctx.strokeStyle = rgba(color, active ? 0.58 : link.depth === 1 ? 0.28 : 0.13);
    ctx.lineWidth = active ? 1.7 : link.depth === 1 ? 1.1 : 0.7;
    ctx.stroke();
  });
}

function drawNodes() {
  const sorted = [...state.graph.nodes].sort((a, b) => b.depth - a.depth);
  sorted.forEach((node) => {
    const point = worldToScreen(node);
    const color = colorFor(node.category);
    const radius = clamp(node.r * Math.sqrt(state.view.scale), node.depth === 0 ? 18 : 4.5, node.depth === 0 ? 42 : 20);
    const isHover = state.hover?.label === node.label;
    const isSelected = state.selected === node.label;

    ctx.save();
    ctx.beginPath();
    ctx.arc(point.x, point.y, radius + (isHover ? 3 : 0), 0, Math.PI * 2);
    ctx.shadowColor = rgba(color, isHover || isSelected ? 0.34 : 0.18);
    ctx.shadowBlur = isHover || isSelected ? 18 : 8;
    ctx.fillStyle = color;
    ctx.globalAlpha = node.depth > 1 ? 0.88 : 0.96;
    ctx.fill();
    ctx.restore();

    ctx.beginPath();
    ctx.arc(point.x, point.y, radius, 0, Math.PI * 2);
    ctx.fillStyle = color;
    ctx.fill();

    ctx.beginPath();
    ctx.arc(point.x - radius * 0.25, point.y - radius * 0.28, Math.max(1.5, radius * 0.26), 0, Math.PI * 2);
    ctx.fillStyle = "rgba(255, 255, 255, 0.28)";
    ctx.fill();

    if (isSelected || node.depth === 0) {
      ctx.beginPath();
      ctx.arc(point.x, point.y, radius + 5, 0, Math.PI * 2);
      ctx.strokeStyle = "rgba(17, 24, 39, 0.24)";
      ctx.lineWidth = 2;
      ctx.stroke();
    }
  });
}

function shouldLabel(node) {
  if (node.depth === 0 || node.depth === 1) return true;
  if (state.hover?.label === node.label || state.selected === node.label) return true;
  const threshold = state.labelDensity * 1000;
  return (hashString(node.label) % 1000) < threshold && state.view.scale > 0.42;
}

function drawLabels() {
  const labeled = state.graph.nodes.filter(shouldLabel).sort((a, b) => b.weight - a.weight);
  labeled.forEach((node) => {
    const point = worldToScreen(node);
    const isRoot = node.depth === 0;
    const isMajor = node.depth === 1;
    const baseSize = isRoot ? 30 : isMajor ? 14 : 10.5;
    const fontSize = clamp(baseSize * Math.sqrt(state.view.scale), isRoot ? 20 : 8.5, isRoot ? 42 : isMajor ? 18 : 13);
    const weight = isRoot ? 800 : isMajor ? 720 : 600;
    const font = `${weight} ${fontSize}px Inter, "Microsoft YaHei", sans-serif`;
    const text = truncateText(node.label, isRoot ? 360 : isMajor ? 170 : 120, font);
    const offset = clamp(node.r * Math.sqrt(state.view.scale) + 4, 10, 30);

    ctx.font = font;
    ctx.textAlign = "center";
    ctx.textBaseline = isRoot ? "middle" : "top";
    ctx.lineJoin = "round";
    ctx.miterLimit = 2;
    ctx.strokeStyle = "rgba(255, 255, 255, 0.92)";
    ctx.lineWidth = isRoot ? 7 : 4;
    ctx.strokeText(text, point.x, isRoot ? point.y : point.y + offset);
    ctx.fillStyle = isRoot ? "#111827" : "rgba(20, 24, 33, 0.92)";
    ctx.fillText(text, point.x, isRoot ? point.y : point.y + offset);
  });
}

function hitTest(screenPoint) {
  const candidates = [...state.graph.nodes].sort((a, b) => b.r - a.r);
  return candidates.find((node) => {
    const point = worldToScreen(node);
    const radius = clamp(node.r * Math.sqrt(state.view.scale), node.depth === 0 ? 18 : 6, node.depth === 0 ? 42 : 22);
    const dx = screenPoint.x - point.x;
    const dy = screenPoint.y - point.y;
    return Math.sqrt(dx * dx + dy * dy) <= radius + 6;
  }) || null;
}

function pointerPosition(event) {
  const rect = canvas.getBoundingClientRect();
  return { x: event.clientX - rect.left, y: event.clientY - rect.top };
}

function showTooltip(node, screenPoint) {
  if (!node) {
    tooltip.hidden = true;
    return;
  }
  tooltip.hidden = false;
  tooltip.innerHTML = `<strong>${node.label}</strong>${escapeHtml(conceptDefinitionFor(node.label, node.category))}`;
  const x = Math.min(screenPoint.x + 16, state.size.width - tooltip.offsetWidth - 18);
  const y = Math.min(screenPoint.y + 16, state.size.height - tooltip.offsetHeight - 18);
  tooltip.style.left = `${Math.max(12, x)}px`;
  tooltip.style.top = `${Math.max(12, y)}px`;
}

function updateInspector() {
  const selectedNode = state.graph.nodes.find((node) => node.label === state.selected) || state.graph.nodes[0];
  document.getElementById("currentConcept").textContent = `知识体系：${state.root}`;
  document.getElementById("nodeCount").textContent = `${state.graph.nodes.length} 个概念`;
  document.getElementById("linkCount").textContent = `${state.graph.links.length} 条关系`;
  document.getElementById("detailTitle").textContent = selectedNode?.label || state.root;
  document.getElementById("detailType").textContent = selectedNode?.category || "中心概念";
  document.getElementById("detailSummary").textContent = selectedNode?.depth === 0 ? rootSummaryFor(state.root) : selectedNode?.summary || summaryFor(state.root);
  document.getElementById("deepExplanation").innerHTML = renderDeepExplanation(
    selectedNode?.depth === 0 ? rootDeepExplanationFor(state.root) : nodeDeepExplanationFor(selectedNode)
  );
  document.querySelectorAll("#deepExplanation button[data-concept]").forEach((button) => {
    button.addEventListener("click", () => {
      const concept = button.dataset.concept;
      if (concept) setRoot(concept, true, { forceFresh: true });
    });
  });

  const relationList = document.getElementById("relationList");
  const rootLinks = state.graph.links
    .filter((link) => link.source.label === state.root || link.target.label === state.root)
    .sort((a, b) => b.strength - a.strength)
    .slice(0, 10);
  relationList.innerHTML = rootLinks
    .map((link) => {
      const target = link.source.label === state.root ? link.target : link.source;
      const color = colorFor(target.category);
      return `
        <div class="relation-item">
          <span class="relation-dot" style="background:${color}"></span>
          <div>
            <button type="button" data-concept="${escapeHtml(target.label)}">${escapeHtml(target.label)}</button>
            <p>${escapeHtml(link.label)} · ${escapeHtml(target.category)}</p>
          </div>
        </div>
      `;
    })
    .join("");

  relationList.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => setRoot(button.dataset.concept));
  });

  const historyList = document.getElementById("historyList");
  historyList.innerHTML = state.history
    .map((item) => `<button type="button" data-concept="${escapeHtml(item)}">${escapeHtml(item)}</button>`)
    .join("");
  historyList.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => setRoot(button.dataset.concept));
  });

  const categories = [...new Set(state.graph.nodes.map((node) => node.category))]
    .sort((a, b) => CATEGORY_ORDER.indexOf(a) - CATEGORY_ORDER.indexOf(b));
  document.getElementById("legend").innerHTML = categories
    .map((category) => `
      <div class="legend-item">
        <span class="legend-swatch" style="background:${colorFor(category)}"></span>
        <span>${escapeHtml(category)}</span>
      </div>
    `)
    .join("");
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function bindEvents() {
  conceptForm.addEventListener("submit", (event) => {
    event.preventDefault();
    setRoot(conceptInput.value, true, { forceFresh: true });
  });

  document.getElementById("homeButton").addEventListener("click", () => setRoot("自由"));
  document.getElementById("resetViewButton").addEventListener("click", () => {
    resizeCanvas();
    state.graph.nodes.forEach((node) => {
      node.fixed = false;
      node.vx = 0;
      node.vy = 0;
    });
    resetView();
    state.alpha = 0.85;
    draw();
  });

  document.getElementById("expandButton").addEventListener("click", () => {
    state.scope = Math.min(260, state.scope + 30);
    scopeSlider.value = String(state.scope);
    setRoot(state.root, false);
  });

  scopeSlider.addEventListener("input", () => {
    state.scope = Number(scopeSlider.value);
    setRoot(state.root, false);
  });

  labelSlider.addEventListener("input", () => {
    state.labelDensity = Number(labelSlider.value);
  });

  physicsToggle.addEventListener("change", () => {
    state.physics = physicsToggle.checked;
    state.alpha = Math.max(state.alpha, 0.35);
  });

  canvas.addEventListener("pointerdown", (event) => {
    canvas.setPointerCapture(event.pointerId);
    const point = pointerPosition(event);
    const node = hitTest(point);
    state.pointerMoved = false;
    canvas.classList.add("dragging");

    if (node) {
      state.selected = node.label;
      state.drag = { node, last: point };
      node.fixed = true;
      updateInspector();
    } else {
      state.pan = { last: point };
    }
  });

  canvas.addEventListener("pointermove", (event) => {
    const point = pointerPosition(event);
    if (state.drag) {
      const world = screenToWorld(point);
      state.drag.node.x = world.x;
      state.drag.node.y = world.y;
      state.drag.node.vx = 0;
      state.drag.node.vy = 0;
      state.pointerMoved = true;
      state.alpha = Math.max(state.alpha, 0.3);
      showTooltip(state.drag.node, point);
      return;
    }
    if (state.pan) {
      state.view.x += point.x - state.pan.last.x;
      state.view.y += point.y - state.pan.last.y;
      state.pan.last = point;
      state.pointerMoved = true;
      return;
    }

    state.hover = hitTest(point);
    showTooltip(state.hover, point);
  });

  canvas.addEventListener("pointerup", () => {
    const dragged = state.drag?.node || null;
    if (dragged) {
      dragged.fixed = false;
      if (!state.pointerMoved) setRoot(dragged.label);
    }
    state.drag = null;
    state.pan = null;
    canvas.classList.remove("dragging");
  });

  canvas.addEventListener("pointerleave", () => {
    state.hover = null;
    tooltip.hidden = true;
  });

  canvas.addEventListener(
    "wheel",
    (event) => {
      event.preventDefault();
      const point = pointerPosition(event);
      const before = screenToWorld(point);
      const zoom = event.deltaY > 0 ? 0.9 : 1.1;
      state.view.scale = clamp(state.view.scale * zoom, 0.25, 3.2);
      state.view.x = point.x - before.x * state.view.scale;
      state.view.y = point.y - before.y * state.view.scale;
    },
    { passive: false }
  );

  window.addEventListener("resize", () => {
    resizeCanvas();
    resetView();
  });
}

function animate() {
  if (state.physics) {
    stepSimulation();
    stepSimulation();
  }
  draw();
  requestAnimationFrame(animate);
}

function init() {
  resizeCanvas();
  bindEvents();
  const initialConcept = conceptFromHash() || "自由";
  setRoot(initialConcept, false, { replaceHash: true, forceFresh: true });
  if (!state.history.includes("自由")) state.history.unshift("自由");
  updateInspector();
  window.addEventListener("hashchange", () => {
    const concept = conceptFromHash();
    if (concept && concept !== state.root) {
      setRoot(concept, true, { fromHash: true, forceFresh: true });
    }
  });
  window.philoGraph = {
    getState: () => ({
      root: state.root,
      selected: state.selected,
      generation: state.generation,
      system: state.system
        ? {
            concept: state.system.concept,
            category: state.system.category,
            frame: state.system.frame
          }
        : null,
      view: { ...state.view },
      size: { ...state.size },
      nodes: state.graph.nodes.map((node) => ({
        label: node.label,
        category: node.category,
        depth: node.depth,
        x: node.x,
        y: node.y,
        r: node.r
      })),
      links: state.graph.links.map((link) => ({
        source: link.source.label,
        target: link.target.label,
        label: link.label
      }))
    }),
    setRoot: (label) => setRoot(label),
    resetView
  };
  requestAnimationFrame(animate);
}

init();
